package Entity;

/**
 *
 * @author Marcelo
 */
public class Notify {
    private int marchaTentada;
    private int marchaAtual;
    private boolean result;

    public int getMarchaTentada() {
        return marchaTentada;
    }

    public void setMarchaTentada(int marchaTentada) {
        this.marchaTentada = marchaTentada;
    }

    public int getMarchaAtual() {
        return marchaAtual;
    }

    public void setMarchaAtual(int marchaAtual) {
        this.marchaAtual = marchaAtual;
    }

    public boolean isResult() {
        return result;
    }

    public void setResult(boolean result) {
        this.result = result;
    } 
    
}
